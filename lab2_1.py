финансовая_подушка = 20000  # Подушка безопасности
зарплата = 5000  # Ежемесячная зарплата
расходы = 6000  # Траты за первый месяц
рост_цен = 0.05  # Ежемесячный рост цен

месяцы = 0  # Количество месяцев без долгов

for месяц in range(1, 1000):  # Ограничим большим числом
    if финансовая_подушка + зарплата >= расходы:
        финансовая_подушка = финансовая_подушка + зарплата - расходы
        расходы *= (1 + рост_цен)
        месяцы += 1
    else:
        break

print("Количество месяцев, которое можно протянуть без долгов:", месяцы)